// Constructor for the Obstacle class, which generates a set of blocks based on the predefined grid
Obstacle::Obstacle(Vector2 position) {
    this->position = position;  // Set the position where the obstacle grid will be placed

    // Iterate through the grid and create blocks wherever there is a '1' (obstacle)
    for (unsigned int row = 0; row < grid.size(); ++row) {
        for (unsigned int column = 0; column < grid[0].size(); ++column) {
            if (grid[row][column]) {  // If the grid value is '1', create an obstacle block
                float pos_x = position.x + column * 3;  // Calculate the x-coordinate of the block
                float pos_y = position.y + row * 3;     // Calculate the y-coordinate of the block
                Block block = Block({ pos_x, pos_y });  // Create a Block object at the calculated position
                blocks.push_back(block);  // Add the block to the list of blocks
            }
        }
    }
}

// Draws all the blocks of the obstacle on the screen
void Obstacle::Draw() {
    for (auto& block : blocks) {  // Iterate through each block in the blocks list
        block.Draw();  // Call the Draw method of each block to render it on screen
    }
}
